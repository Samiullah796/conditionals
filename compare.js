// Comparison operators
let x = 2;            // Initialize the variable
console.log(x);       // Prints: 2

console.log(x == 2);  // Compare x to 2, prints: true

console.log(x >= 3);  // Compare x to 3, prints: false

console.log(x <= 2);  // Compare x to 2, prints: true

console.log(x != 6);  // Compare x to 6, prints: true

console.log(x === "2");  // Strict comparison, prints: false (since 2 is a number, and "2" is a string)

console.log(x !== "2");  // Strict inequality, prints: true

console.log(x > 2);   // Compare x to 2, prints: false

console.log(x < 2);   // Compare x to 2, prints: false

